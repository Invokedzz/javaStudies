package src.baldFlakes2;

public class whileAgain {

    public static void main (String[] args) {

        int valueA, valueB;

        valueA = 0;

        valueB = 16;

        while (valueA < 5) {

            valueA = valueA + 1;

            valueB = valueB + 3;

            System.out.println(valueA + " " + valueB);

        }

    }

}
